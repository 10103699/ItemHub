@Composable
fun NormalText(value: String){
    Text(
        modifier = Modifier
            .fillMaxWidth()
            .heightIn(min = 80.dp),
        text = value,
        style = TextStyle(
            fontSize = 18.sp,
            fontWeight = FontWeight.Normal,
            fontStyle = FontStyle.Normal
        ),
        color = colorResource(id = R.color.white)
    )
}

